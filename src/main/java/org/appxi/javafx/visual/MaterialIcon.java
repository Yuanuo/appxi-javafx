    public final String text;

    MaterialIcon(char ch) {
        this.text = Character.toString(ch);
    }

    public Node graphic() {
        return this.graphic(null);
    }

    public Node graphic(String style) {
        Label label = new Label(this.text);
        label.getStyleClass().add("icon-text");
        label.setAlignment(Pos.CENTER);
        label.setStyle("-fx-padding:0;".concat(style != null ? style : ""));
        return label;
    }

    public Button iconButton() {
        return this.iconButton(null);
    }

    public Button iconButton(EventHandler<ActionEvent> onAction) {
        Button button = new Button(this.text);
        button.getStyleClass().addAll("icon-toggle");
        button.setOnAction(onAction);
        return button;
    }

    public Button flatButton() {
        return this.flatButton(null);
    }

    public Button flatButton(EventHandler<ActionEvent> onAction) {
        Button button = new Button(null, graphic());
        button.getStyleClass().addAll("flat");
        button.setOnAction(onAction);
        return button;
    }

    public ToggleButton flatToggle() {
        return this.flatToggle(null);
    }

    public ToggleButton flatToggle(EventHandler<ActionEvent> onAction) {
        ToggleButton button = new ToggleButton(null, graphic());
        button.getStyleClass().addAll("flat");
        button.setOnAction(onAction);
        return button;
    }
